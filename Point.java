package artist;
import artist.StdDraw;

public class Point {
  public double x;
  public double y;
  /*
   * This method: Point, sets where x and y on canvas
   * 
   * @param: double x, double y
   * 
   * return type: no return type because it is a constructor
   */
  public Point(double x, double y) {
    this.x = x;
    this.y = y;
  }
}
